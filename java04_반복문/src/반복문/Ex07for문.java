package 반복문;

public class Ex07for문 {

	public static void main(String[] args) {
		
//		Q. 1부터 5까지 출력 해주는 반복문을 작성하시오
		for(int i = 1; i<=5; i++) { // 1부터 5까지
			System.out.println(i);
		}

//		Q. 1부터 5까지 출력 해주는 반복문을 작성하시오
		for(int i = 0; i<=9; i++) { // 0부터 9까지 반복 -> 총 10번 반복
			System.out.println("hello");
		}


	}

}
